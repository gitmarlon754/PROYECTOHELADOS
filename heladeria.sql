CREATE TABLE CATEGORIA (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(255) NOT NULL,

    CONSTRAINT PK_CATEGORIA PRIMARY KEY (ID),
    CONSTRAINT UQ_CATEGORIA_NOMBRE UNIQUE (NOMBRE)
);



CREATE TABLE TIPO_HELADO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(255) NOT NULL,

    CONSTRAINT PK_TIPO_HELADO PRIMARY KEY (ID),
    CONSTRAINT UQ_TIPO_HELADO_NOMBRE UNIQUE (NOMBRE)
);


CREATE TABLE SEDE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(255) NOT NULL,
    DIRECCION VARCHAR2(255),

    CONSTRAINT PK_SEDE PRIMARY KEY (ID)
);


CREATE TABLE PRODUCTO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(255) NOT NULL,
    PRECIO NUMBER(10,2) NOT NULL,
    CATEGORIA_ID NUMBER NOT NULL,
    TIPO_HELADO_ID NUMBER,
    ACTIVO NUMBER(1) DEFAULT 1,
    IMAGEN VARCHAR2(255),

    CONSTRAINT PK_PRODUCTO PRIMARY KEY (ID),

    CONSTRAINT FK_PRODUCTO_CATEGORIA
        FOREIGN KEY (CATEGORIA_ID) REFERENCES CATEGORIA(ID),

    CONSTRAINT FK_PRODUCTO_TIPO
        FOREIGN KEY (TIPO_HELADO_ID) REFERENCES TIPO_HELADO(ID)
);


CREATE TABLE VENTA (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    SEDE_ID NUMBER NOT NULL,
    FECHA TIMESTAMP DEFAULT SYSTIMESTAMP,
    TOTAL NUMBER(10,2) NOT NULL,

    CONSTRAINT PK_VENTA PRIMARY KEY (ID),

    CONSTRAINT FK_VENTA_SEDE
        FOREIGN KEY (SEDE_ID) REFERENCES SEDE(ID)
);


CREATE TABLE DETALLE_VENTA (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    VENTA_ID NUMBER NOT NULL,
    PRODUCTO_ID NUMBER NOT NULL,
    CANTIDAD NUMBER NOT NULL,
    PRECIO_UNITARIO NUMBER(10,2) NOT NULL,

    CONSTRAINT PK_DETALLE_VENTA PRIMARY KEY (ID),

    CONSTRAINT FK_DETALLE_VENTA
        FOREIGN KEY (VENTA_ID) REFERENCES VENTA(ID),

    CONSTRAINT FK_DETALLE_PRODUCTO
        FOREIGN KEY (PRODUCTO_ID) REFERENCES PRODUCTO(ID)
);

CREATE TABLE INVENTARIO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PRODUCTO_ID NUMBER NOT NULL,
    SEDE_ID NUMBER NOT NULL,
    CANTIDAD NUMBER NOT NULL,

    CONSTRAINT PK_INVENTARIO PRIMARY KEY (ID),

    CONSTRAINT UQ_INVENTARIO UNIQUE (PRODUCTO_ID, SEDE_ID),

    CONSTRAINT FK_INVENTARIO_PRODUCTO
        FOREIGN KEY (PRODUCTO_ID) REFERENCES PRODUCTO(ID),

    CONSTRAINT FK_INVENTARIO_SEDE
        FOREIGN KEY (SEDE_ID) REFERENCES SEDE(ID)
);

